export const getFilteredTickers = (query: string): string[] => {
  const normalizedQuery = query.toUpperCase().trim();
  if (!normalizedQuery) return [];

  const filtered = COMMON_TICKERS.filter((ticker) =>
    ticker.toUpperCase().includes(normalizedQuery),
  );

  // Deduplicate results (some tickers appear multiple times in COMMON_TICKERS)
  const uniqueFiltered = [...new Set(filtered)];

  // Sort by relevance: exact matches first, then prefix matches, then substring matches
  const sorted = uniqueFiltered.sort((a, b) => {
    const aUpper = a.toUpperCase();
    const bUpper = b.toUpperCase();

    // Exact match gets highest priority
    const aExact = aUpper === normalizedQuery;
    const bExact = bUpper === normalizedQuery;
    if (aExact && !bExact) return -1;
    if (!aExact && bExact) return 1;
    if (aExact && bExact) return 0;

    // Prefix match (starts with) gets second priority
    const aStartsWith = aUpper.startsWith(normalizedQuery);
    const bStartsWith = bUpper.startsWith(normalizedQuery);
    if (aStartsWith && !bStartsWith) return -1;
    if (!aStartsWith && bStartsWith) return 1;

    // Within same category, alphabetical order
    return aUpper.localeCompare(bUpper);
  });

  return sorted.slice(0, 100);
};
